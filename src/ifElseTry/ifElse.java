package ifElseTry;

public class ifElse {

	public static void main(String[] args) {
		int day = 3;
		switch (day) {
		case 1:
			System.out.println("Today is Sunday");
			break;
		case 2:
			System.out.println("Today is Monday");
			break;
		case 4:
			System.out.println("Today is Wednesday");
			break;
			default:
				System.out.println("Today is Off");
			
		}
	}
	}

